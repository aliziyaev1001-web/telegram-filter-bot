async def send_question(context, uid):
    session = sessions.get(uid)
    if not session:
        return
    idx = session["current"]
    total = len(session["questions"])
    q = session["questions"][idx]
    options = q["opts"].copy()
    random.shuffle(options)
    session["shuffled_opts"] = options
    hearts = "❤️" * session["lives"] + "🖤" * (3 - session["lives"])
    streak_text = f" 🔥×{session['streak']}" if session["streak"] >= 2 else ""
    keyboard = [[InlineKeyboardButton(opt, callback_data=f"ans:{uid}:{i}")] for i, opt in enumerate(options)]
    extra = []
    if session["hints"] > 0:
        extra.append(InlineKeyboardButton(f"💡 Подсказка ({session['hints']})", callback_data=f"hint:{uid}"))
    extra.append(InlineKeyboardButton("🏳 Сдаться", callback_data=f"giveup:{uid}"))
    keyboard.append(extra)
    text = (f"{hearts}{streak_text}\n❓ <b>Вопрос {idx+1}/{total}</b> • ✅ {session['score']}\n\n<b>{q['q']}</b>")
    msg = await context.bot.send_message(chat_id=session["chat_id"], text=text, reply_markup=InlineKeyboardMarkup(keyboard), parse_mode="HTML")
    session["last_msg_id"] = msg.message_id


async def handle_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    d = query.data
    user = query.from_user
    uid = str(user.id)

    if d == "menu_main":
        await show_main_menu(update, context, user)
    elif d == "menu_play":
        await cmd_quiz(update, context)
    elif d == "menu_profile":
        await cmd_profile(update, context)
    elif d == "menu_top":
        await cmd_top(update, context)
    elif d == "menu_help":
        text = ("📖 <b>Как играть:</b>\n\n1️⃣ Выбери категорию и количество вопросов\n2️⃣ Отвечай нажимая кнопки\n"
                "3️⃣ У тебя 3 жизни ❤️\n4️⃣ За серию правильных ответов — бонус 🔥\n5️⃣ Используй подсказки 💡 (3 за игру)\n\n"
                "⭐ <b>XP за игру:</b>\n• Правильный ответ: +10 XP\n• Серия 3+: +5 XP бонус\n• Серия 5+: +10 XP бонус\n• Подсказка: -5 XP\n\n"
                "🎖 <b>Уровни:</b>\n" + "\n".join(f"• {name} (от {xp} XP)" for xp, name in LEVELS))
        kb = InlineKeyboardMarkup([[InlineKeyboardButton("◀️ Назад", callback_data="menu_main")]])
        await query.edit_message_text(text, parse_mode="HTML", reply_markup=kb)

    elif d.startswith("cat:"):
        cat_id = d.split(":")[1]
        await query.edit_message_text("⚡ <b>Выбери количество вопросов:</b>", parse_mode="HTML", reply_markup=difficulty_kb(cat_id))

    elif d.startswith("diff:"):
        _, cat_id, count = d.split(":")
        count = int(count)
        if cat_id == "random":
            all_q = [q for ql in QUESTIONS.values() for q in ql]
            questions = random.sample(all_q, min(count, len(all_q)))
        else:
            questions = random.sample(QUESTIONS.get(cat_id, []), min(count, len(QUESTIONS.get(cat_id, []))))
        sessions[uid] = {"questions": questions, "current": 0, "score": 0, "lives": 3, "hints": 3, "streak": 0, "best_streak": 0, "xp_earned": 0, "chat_id": query.message.chat_id, "name": user.full_name or "Игрок", "shuffled_opts": []}
        cat_name = next((k for k, v in CATEGORIES.items() if v == cat_id), "Случайные")
        await query.edit_message_text(f"🎮 <b>Старт!</b>\n\n📚 {cat_name}\n❓ Вопросов: {count}\n❤️ Жизней: 3 | 💡 Подсказок: 3\n\nУдачи, <b>{user.first_name}</b>! 🍀", parse_mode="HTML")
        await send_question(context, uid)

    elif d.startswith("hint:"):
        target = d.split(":")[1]
        if uid != target:
            await query.answer("Это не твоя игра!", show_alert=True); return
        session = sessions.get(uid)
        if not session or session["hints"] <= 0:
            await query.answer("Подсказки закончились!", show_alert=True); return
        session["hints"] -= 1
        session["xp_earned"] = max(0, session["xp_earned"] - 5)
        q = session["questions"][session["current"]]
        u = get_user(db, uid, user.full_name or "Игрок")
        u["hints_used"] = u.get("hints_used", 0) + 1
        save_data(db)
        await query.answer("💡 Подсказка отправлена!")
        await query.message.reply_text(f"💡 <b>Подсказка:</b> {q['hint']}\n\n(-5 XP)", parse_mode="HTML")

    elif d.startswith("giveup:"):
        target = d.split(":")[1]
        if uid != target:
            await query.answer("Это не твоя игра!", show_alert=True); return
        session = sessions.pop(uid, None)
        if not session: return
        u = get_user(db, uid, user.full_name or "Игрок")
        u["xp"] = u.get("xp", 0) + session["xp_earned"]
        save_data(db)
        await query.edit_message_text(f"🏳 Игра прервана.\n✅ Правильных: {session['score']}/{session['current']}\n⭐ XP: +{session['xp_earned']}\n\nСыграть ещё: /quiz", parse_mode="HTML")

    elif d.startswith("ans:"):
        parts = d.split(":")
        target, opt_idx = parts[1], int(parts[2])
        if uid != target:
            await query.answer("Это не твоя игра! Начни свою: /quiz", show_alert=True); return
        session = sessions.get(uid)
        if not session:
            await query.edit_message_text("❌ Сессия устарела. Начни новую: /quiz"); return
        idx = session["current"]
        q = session["questions"][idx]
        chosen = session["shuffled_opts"][opt_idx]
        correct = q["a"]
        await query.edit_message_reply_markup(reply_markup=None)
        if chosen == correct:
            session["score"] += 1
            session["streak"] += 1
            session["best_streak"] = max(session["best_streak"], session["streak"])
            xp = 10
            bonus = 10 if session["streak"] >= 5 else (5 if session["streak"] >= 3 else 0)
            session["xp_earned"] += xp + bonus
            streak_text = f"\n🔥 <b>Серия {session['streak']}!</b> +{bonus} XP бонус!" if bonus else (f"\n🔥 Серия {session['streak']}!" if session["streak"] >= 2 else "")
            result = f"✅ <b>Правильно!</b> +{xp} XP{streak_text}"
        else:
            session["streak"] = 0
            session["lives"] -= 1
            u = get_user(db, uid, user.full_name or "Игрок")
            u["lives_used"] = u.get("lives_used", 0) + 1
            save_data(db)
            hearts = "❤️" * session["lives"] + "🖤" * (3 - session["lives"])
            result = f"❌ <b>Неверно!</b>\n✔️ Правильный ответ: <b>{correct}</b>\n{hearts}"
        await query.message.reply_text(f"{result}\n📊 Счёт: {session['score']}/{idx+1}", parse_mode="HTML")
        session["current"] += 1
        if session["lives"] <= 0 or session["current"] >= len(session["questions"]):
            await finish_game(query, context, uid, session["lives"] <= 0)
        else:
            await send_question(context, uid)

    elif d.startswith("play_again:") or d.startswith("menu_"):
        pass


async def finish_game(query, context, uid, lost_lives=False):
    session = sessions.pop(uid, None)
    if not session: return
    score, total, xp = session["score"], len(session["questions"]), session["xp_earned"]
    streak, name = session["best_streak"], session["name"]
    percent = int(score / total * 100)
    u = get_user(db, uid, name)
    u["xp"] = u.get("xp", 0) + xp
    u["total_games"] = u.get("total_games", 0) + 1
    u["total_correct"] = u.get("total_correct", 0) + score
    u["best_streak"] = max(u.get("best_streak", 0), streak)
    is_record = score > u.get("best_score", 0)
    if is_record: u["best_score"] = score
    save_data(db)
    level = get_level(u["xp"])
    if lost_lives: title = "💔 <b>Жизни закончились!</b>"
    elif percent == 100: title = "🏆 <b>Идеально! Ты гений!</b>"
    elif percent >= 80: title = "🌟 <b>Отличный результат!</b>"
    elif percent >= 60: title = "👍 <b>Хороший результат!</b>"
    elif percent >= 40: title = "😐 <b>Неплохо!</b>"
    else: title = "😅 <b>Нужно больше практики!</b>"
    record_text = "\n🎉 <b>Новый рекорд!</b>" if is_record else ""
    text = (f"{title}\n\n✅ Правильных: <b>{score}/{total}</b> ({percent}%)\n⭐ XP: <b>+{xp}</b>\n"
            f"🔥 Серия: <b>{streak}</b>\n🎖 Уровень: <b>{level}</b>\n⭐ Всего XP: <b>{u['xp']}</b>{record_text}")
    kb = InlineKeyboardMarkup([
        [InlineKeyboardButton("🔄 Ещё раз", callback_data="menu_play"), InlineKeyboardButton("👤 Профиль", callback_data="menu_profile")],
        [InlineKeyboardButton("🏆 Топ", callback_data="menu_top"), InlineKeyboardButton("🏠 Меню", callback_data="menu_main")],
    ])
    await query.message.reply_text(text, parse_mode="HTML", reply_markup=kb)


