def load_data():
    if os.path.exists(DATA_FILE):
        try:
            with open(DATA_FILE, "r", encoding="utf-8") as f:
                return json.load(f)
        except Exception:
            pass
    return {"users": {}}


def save_data(d):
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(d, f, ensure_ascii=False, indent=2)


def get_user(d, uid, name):
    if uid not in d["users"]:
        d["users"][uid] = {"name": name, "xp": 0, "total_games": 0, "total_correct": 0, "best_score": 0, "best_streak": 0, "hints_used": 0, "lives_used": 0}
    d["users"][uid]["name"] = name
    return d["users"][uid]


def get_level(xp):
    level = LEVELS[0][1]
    for threshold, name in LEVELS:
        if xp >= threshold:
            level = name
    return level


def get_next_level(xp):
    for i, (threshold, name) in enumerate(LEVELS):
        if xp < threshold:
            prev = LEVELS[i-1][0] if i > 0 else 0
            return name, threshold, threshold - xp
    return "Максимум", 0, 0


