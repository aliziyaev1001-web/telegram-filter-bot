    uid = str(user.id)
    u = get_user(db, uid, user.full_name or "Игрок")
    save_data(db)
    level = get_level(u["xp"])
    text = (f"👋 Привет, <b>{user.first_name}</b>!\n\n🧠 <b>СУПЕР ВИКТОРИНА v2.0</b>\n\n"
            f"🎖 Уровень: {level}\n⭐ XP: {u['xp']}\n\nВыбери действие:")
    if update.message:
        await update.message.reply_text(text, parse_mode="HTML", reply_markup=main_menu_kb())
    else:
        await update.callback_query.edit_message_text(text, parse_mode="HTML", reply_markup=main_menu_kb())


